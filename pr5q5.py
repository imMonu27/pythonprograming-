# sum of natural no using range function

num=int(input("enter no"))
sum=1
for i in range(1,num+1):
    
    sum=i+num
    print(sum)


# sum of natural no in while loop

num=int(input("enter no"))
sum=0
i=1
while(i < num):
    i=i+1
    sum=num+i
    print(f"the sum of {num} is {sum}")

